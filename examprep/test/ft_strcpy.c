char	*ft_strcpy(char *s1, char *s2) 
{//easier to think of with replacing *s1=*dest and *s2=*src
	while (*s2)
	{
		*s1 = *s2;
		s2++;
		s1++;
	}
	*s1 = *s2;
	return (s1);
}
